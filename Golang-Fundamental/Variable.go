package main

import "fmt"

func main() {
	//Define variable
	var age int
	var name string
	var isStudent bool
	var temperature float64

	age = 30
	name = "John Doe"
	isStudent = true
	temperature = 28.90

	fmt.Println("Age:", age)
	fmt.Println("Name:", name)
	fmt.Println("Student:", isStudent)
	fmt.Println("Temperature:", temperature)

	//Define and assign value variable
	width := 45
	height := 100
	fmt.Println("Rectangle with: ", width, "height: ", height)
}
